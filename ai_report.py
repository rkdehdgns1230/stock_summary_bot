from google import genai
import config

client = genai.Client(api_key=config.GEMINI_API_KEY)

def get_expert_guidelines() -> str:
    return """
[전문가의 대응 전략 지침 - 반드시 지킬 것]
1. 절대 특정 종목의 매수/매도를 직접적으로 권유하지 마라.
2. 현재 수집된 시장 지표와 커뮤니티 여론을 바탕으로, 오늘 하루 투자자들이 가져야 할 '마인드셋'과 '리스크 관리 방향'을 제시해라.
3. 시장이 과열 양상을 보일 때는 FOMO(소외 불안감)에 휩쓸려 비이성적인 목돈 투자를 감행하는 것을 엄격하게 경계해라.
4. 변동성이 심한 장세에서는 무엇보다 '잃지 않는 투자'가 최우선임을 상기시키고, 현금 비중 유지나 반드시 '분할 매수'로 접근해야 한다는 원칙을 조언해라.
5. "오늘 국장 관전 포인트:" 라는 소제목 아래, 한국 증시에서 주의 깊게 봐야 할 업종이나 외인/기관 수급 동향 등 객관적인 참고 사항을 2~3가지 불렛 포인트로 요약해라.
"""

def get_persona_instruction(score: int) -> str:
    """공포·탐욕 지수 점수에 따라 AI 페르소나 지침 반환"""
    if score < 25:
        return "너는 지금 시장이 붕괴될까 봐 공포에 질린 '겁쟁이 투자자'야. 지표에서 위험 신호를 찾아내고, 투자자들에게 조심하라고 경고하는 말투를 사용해."
    elif score < 45:
        return "너는 아주 보수적인 '염세주의 분석가'야. 상승장 속에서도 숨은 위험을 찾아내고, 독설을 섞어가며 조심하라고 경고해."
    elif score < 55:
        return "너는 감정이 없는 '퀀트 분석 로봇'이야. 철저하게 수치와 팩트 중심으로만, 군더더기 없이 딱딱하게 보고해."
    elif score < 75:
        return "너는 에너지가 넘치는 '낙관주의 트레이더'야. 시장의 기회를 강조하고, 투자자들에게 희망과 자신감을 주는 말투를 사용해."
    else:
        return "너는 지금 탐욕에 눈이 먼 '광기 어린 투기꾼'이야. 무조건 상승한다고 외치고, 이모지를 남발하며 매우 흥분된 상태로 가즈아를 외쳐!"


def build_prompt(today: str, persona_instruction: str, us_data: str,
                 kospi_data: str, kosdaq_data: str, fng_stage: str, score: int, news_data: str) -> str:
    """Gemini에 전달할 프롬프트 구성"""
    return f"""
[AI 페르소나 지침]
{persona_instruction}
위 가이드라인을 철저하게 지켜서, 오늘의 시장 상황을 분석하고 투자자들에게 유용한 인사이트를 제공하는 리포트를 작성해라.

[임무]
오늘({today}) 아침 한국 주식시장 개장 전, 투자자를 위한 *모닝 브리핑 리포트*를 작성해야 한다.
아래 데이터는 자동 수집된 것으로 일부 항목이 누락되거나 "데이터 부족", "수집 실패" 상태일 수 있다.
*누락·실패 항목은 절대 "데이터 없음"으로 건너뛰지 말고*, 나머지 지표들 간의 상관관계와 시장 맥락을 활용해 합리적으로 추론하여 최선의 분석을 제공해야 한다.

[입력 데이터]
1. 미국 시장 지표 (전일 종가 기준):
{us_data}

2. 코스피 지수 (전일 종가 기준):
{kospi_data}

3. 코스닥 지수 (전일 종가 기준):
{kosdaq_data}

4. CNN 공포·탐욕 지수: {fng_stage} ({score}/100)
   - 0~24: 극도의 공포 / 25~44: 공포 / 45~55: 중립 / 56~75: 탐욕 / 76~100: 극도의 탐욕

5. 국내 증권 주요 뉴스 (네이버 금융):
{news_data}

[분석 사고 흐름 — 반드시 이 순서로 내부적으로 추론한 뒤 리포트를 작성해라]
Step 1. 미국 지수(나스닥·S&P500)의 방향성과 변동폭이 한국 시장에 주는 선행 시그널을 파악한다.
Step 2. 환율(USD/KRW)과 미 10년물 국채 금리가 외국인 자금 흐름 및 성장주 밸류에이션에 미치는 영향을 판단한다.
Step 3. 공포·탐욕 지수가 현재 시장 심리의 과열·공포 구간 여부를 나타내는지 해석하고, 반전 가능성을 고려한다.
Step 4. 뉴스 헤드라인에서 당일 한국 증시에 직접 영향을 줄 핵심 이슈(정책, 실적, 지정학적 리스크 등)를 추출한다.
Step 5. 위 네 가지 요소를 종합하여 오늘 코스피 및 코스닥의 상승/하락 가능성과 강도를 결론 짓는다.
Step 6. 특정 데이터가 누락된 경우, 다른 지표들을 근거로 삼아 그 항목을 최대한 추론·보완한다.

[출력 구조 — 아래 6개 섹션을 순서대로 작성할 것]
- 섹션 헤더는 반드시 __이모지 헤더텍스트__ (밑줄) 형식으로 작성할 것
- 섹션 사이에는 반드시 ━━━━━━━━━━ 구분선을 삽입할 것

1. __📋 오늘의 한 줄 요약__
   헤더 다음 줄에 반드시 >페르소나 어조의 한 문장을 인용 블록(blockquote)으로 작성

2. __🌏 글로벌 시장 동향__
   수집된 수치 데이터(지수값, 환율, 금리)는 아래처럼 코드 블록으로 묶을 것:
   ```
   나스닥: 18,234.56 (+2.35%)
   S&P500: 5,123.45 (+1.12%)
   코스피: 5,345.67 (-0.45%)
   코스닥: 1,000.12 (+0.78%)
   환율: 1,320.50 원
   미 10년물: 4.25%
   ```
   이후 한국 시장 영향 분석을 추가로 일반 텍스트로 서술

3. __📰 핵심 뉴스 & 이슈__
   오늘 시장에 영향을 줄 상위 3~5개 뉴스를 한 줄씩 요약

4. __🔮 오늘의 증시 전망__
   분석 내용은 일반 텍스트로 서술하고, 최종 결론 한 줄만 ||스포일러||로 감싸서 클릭 유도

5. __🎯 대응 전략__
   한국·미국 시장 포지션 제안 (매수/매도/관망, 주목 섹터)

6. __⚠️ 리스크 요인__
   전망을 뒤집을 수 있는 핵심 변수 1~3가지

[Markdown 규칙 — 텔레그램 MarkdownV2 전용]
- 섹션 헤더 밑줄: __텍스트__
- 굵게: *텍스트* (별표 1개)
- 기울임: _텍스트_ (언더스코어 1개)
- 인용 블록: >텍스트 (줄 맨 앞에 > 붙이기)
- 스포일러: ||텍스트||
- 코드 블록: ```텍스트```
- 인라인 코드: `텍스트`
- 절대 사용 금지: **텍스트** (별표 2개), ### 헤더
- 특수문자 이스케이프는 하지 말 것 (후처리에서 자동 처리됨)
- 이모지는 페르소나 에너지에 맞는 빈도로 사용할 것
"""


def generate_report(today: str, score: int, fng_stage: str,
                    us_data: str, kospi_data: str, kosdaq_data: str, news_data: str) -> str:
    """Gemini API를 호출하여 모닝 브리핑 리포트 텍스트 반환"""
   #  persona_instruction = get_persona_instruction(score)
    market_expert_guidelines = get_expert_guidelines()
    prompt = build_prompt(today, market_expert_guidelines, us_data, kospi_data, kosdaq_data, fng_stage, score, news_data)
    print("[Gemini] 리포트 생성 중...")
    response = client.models.generate_content(model='gemini-3-flash-preview', contents=prompt)
    print("[Gemini] 리포트 생성 완료")
    return response.text
